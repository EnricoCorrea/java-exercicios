package interfaces;
/**
 * Class that represents the Force skills in the Star Wars universe.
 * It is an interface that defines the methods related to Force abilities.
 */
public interface Force {
   /*
    * Method that simulates the mind control skill of the Force user.
    */
   public void mindControl();
   /*
    * Method that simulates the farseeing skill of the Force user.
    */
   public void farseeing();
   /*
    * Method that simulates the telepathy skill of the Force user.
    */
   public void telepath();
   /**
    * Method that simulates the levitation skill of the Force user.
    */
   public void levitation();
   
}